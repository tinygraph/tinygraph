uint32_t tinygraph_hash_u32(uint32_t v) {
  const uint32_t h0 = table[0][(uint8_t)(v >> 8 * 0)];
  const uint32_t h1 = table[1][(uint8_t)(v >> 8 * 1)];
  const uint32_t h2 = table[2][(uint8_t)(v >> 8 * 2)];
  const uint32_t h3 = table[3][(uint8_t)(v >> 8 * 3)];

  return h0 ^ h1 ^ h2 ^ h3;
}


// This is the bit mixer found by computational search
// and a constant added to the seed so that zero does
// not map to zero.
//
// https://nullprogram.com/blog/2018/07/31/
// https://github.com/skeeto/hash-prospector/issues/19
uint32_t tinygraph_hash_combine_u32(uint32_t seed, uint32_t h) {
  uint32_t rv = seed + UINT32_C(0x9e3779b9) + h;

  rv ^= rv >> 16;
  rv *= UINT32_C(0x21f0aaad);
  rv ^= rv >> 15;
  rv *= UINT32_C(0x735a2d97);
  rv ^= rv >> 15;

  return rv;
}
